// Comparison operators

console.log("Comparison operators");
var x=3, y='3';
console.log("x==y: ",x==y); //loose equality
console.log("x===y: ",x===y); //strick equality
console.log("x!=y: ",x!=y); //loose inequality
console.log("x!==y: ",x!==y);  //strick inequality
console.log("x>3: ",x>3);
console.log("x<3: ",x<3);
console.log("x>=3: ",x>=3);
console.log("x<=y: ",x<=y);    